//Write a program to display the size of every data type using “sizeof” operator.

#include<stdio.h>
int main()
{
    printf("\nThe size of char is:%lu\n", sizeof(char));
    printf("\nThe size of int is:%lu\n", sizeof(int));
    printf("\nThe size of float is:%lu\n", sizeof(float));
    printf("\nThe size of double is:%lu\n\n", sizeof(double));
}
